print("##### OPERADORES DE ASIGNACION #####")

a = 10
b = 3

print(f"Operador =: {a}")

a += b
print(f"Operador +=: {a}")

a -= b
print(f"Operador -=: {a}")

a *= b
print(f"Operador *=: {a}")

a /= b
print(f"Operador /=: {a}")

a **= b
print(f"Operador **=: {a}")

a %= b
print(f"Operador %=: {a}")

a //= b
print(f"Operador //=: {a}")

